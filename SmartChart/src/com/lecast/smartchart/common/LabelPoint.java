package com.lecast.smartchart.common;

public class LabelPoint
{

    public LabelPoint(float xLabel, float yLabel, float widthLabel, float size)
    {
        this.xLabel = xLabel;
        this.yLabel = yLabel;
        this.widthLabel = widthLabel;
        this.size = size;
    }

    public float xLabel;

    public float yLabel;

    public float widthLabel;

    public float heightLabel;

    public float size;
}
